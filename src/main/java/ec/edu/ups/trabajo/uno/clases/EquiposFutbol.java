package ec.edu.ups.trabajo.uno.clases;

public class EquiposFutbol 
{
	private String nombreEquipo;
	private int integrantesTotales;
	
	public void setNombreEquipo(String nombreEquipo)
	{
		this.nombreEquipo=nombreEquipo;
	}
	public String getNombreEquipo()
	{
		return nombreEquipo;
	}
	
	public void setIntegrantesTotales (int integrantesTotales)
	{
		this.integrantesTotales=integrantesTotales;
	}
	public int getIntegrantesTotales()
	{
		return integrantesTotales;
	}
}

